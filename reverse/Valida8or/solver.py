numbers = [0x87, 0x81, 0x9e, 0xca, 0xe6, 0x9e, 0xcf, 0x9c, 0xc6, 0xd4, 0xc0, 0xfa, 0xfd, 0xd5, 0x81, 0xc5, 0xbd, 0xa2, 0x87, 0xc8, 0xe5, 0xc6, 0x96]
length = len(numbers)
numbers += [0] * (4 - length % 4)
key = [0xDE, 0xAD, 0xBE, 0xEF]
key2 = 0x3F

for i in range(0, len(numbers), 4):
	for j in range(0, 4):
		numbers[i + j] ^= key2
		key2 += 1
	numbers[i] ^= key[0]
	numbers[i + 1] ^= key[1]
	numbers[i + 2] ^= key[2]
	numbers[i + 3] ^= key[3]

print("".join([chr(c) for c in numbers]))